    protected void SetPanelData(Employee x) {
        NamejTextField.setText("" + x.GetEmployeeName());
        UserNamejTextField.setText("" + x.GetUserName());

        if (x.GetEmplyeeGender().equals("Male")) {
            MalejRadioButton.setSelected(true);
            FemalejRadioButton.setSelected(false);
        } else {
            MalejRadioButton.setSelected(false);
            FemalejRadioButton.setSelected(true);
        }

        PasswordjTextField.setText("" + x.GetPassWord());
        AddressjTextField.setText("" + x.GetEmployeeAddress());
        PhoneNumberjTextField.setText("" + x.GetEmployeePhoneNumber());

        if (x.GetEmployeeQualifications().equals("fresh graduate")) {
            QualificationsjComboBox.setSelectedIndex(0);
        } else if (x.GetEmployeeQualifications().equals("experienced")) {
            QualificationsjComboBox.setSelectedIndex(1);
        } else if (x.GetEmployeeQualifications().equals("student")) {
            QualificationsjComboBox.setSelectedIndex(2);
        }
        DOBjDateChooser.setDate(x.GetEmployeeDateOfBirth());
        DOJjDateChooser.setDate(x.GetEmployeeDateOfJoining());
    }

