    private void SearchEmployeejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchEmployeejButtonActionPerformed

        // TODO add your handling code here:
        if (!SearchEmployeejTextField.getText().equals("")) {
            Employee x = new Employee();
            Employee returned = x.SearchByEmployeeUserName(SearchEmployeejTextField.getText());
            if (returned.GetUserName() != null) {
                SetPanelData(returned);
                Truelabel.setText("");
            } else {
                Truelabel.setText("Not Found ...!");
                SearchEmployeejTextField.setText("");
                ResetPanelData();
            }
        } else {
            Truelabel.setText("Search by user name first !");
        }


    }//GEN-LAST:event_SearchEmployeejButtonActionPerformed

    private void UpdatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatejButtonActionPerformed
        // TODO add your handling code here:
        boolean flag = true;
        if (!UserNamejTextField.getText().equals("") && !PasswordjTextField.getText().equals("")
                && !AddressjTextField.getText().equals("") && !PhoneNumberjTextField.getText().equals("")) {

            Employee x = new Employee();
            x.SetEmployeeName(NamejTextField.getText());
            x.SetUserName(UserNamejTextField.getText());

            if (PasswordjTextField.getText().length() > 8) {
                x.SetPassWord(PasswordjTextField.getText());
                PasswordValidjLabel.setText("");
            } else {
                PasswordValidjLabel.setText("password must be > 8 digits");
                flag = false;
            }

            if (PhoneNumberjTextField.getText().length() != 8) {
                PhonejLabel.setText("not valid phone number (!=8)");
                flag = false;
            } else {
                x.SetEmployeePhoneNumber(Integer.parseInt(PhoneNumberjTextField.getText()));
                PhonejLabel.setText("");
            }
            if (MalejRadioButton.isSelected()) {
                x.SetEmployeeGender("Male");
            } else if (FemalejRadioButton.isSelected()) {
                x.SetEmployeeGender("Female");
            }
            x.SetEmployeeAddress(AddressjTextField.getText());
            x.SetEmployeeDateOfBirth(DOBjDateChooser.getDate());
            x.SetEmployeeDateOfJoining(DOJjDateChooser.getDate());
            if (QualificationsjComboBox.getSelectedItem().equals("fresh graduate")) {
                x.SetEmployeeQualifications("fresh graduate");
            } else if (QualificationsjComboBox.getSelectedItem().equals("experienced")) {
                x.SetEmployeeQualifications("experienced");
            } else if (QualificationsjComboBox.getSelectedItem().equals("student")) {
                x.SetEmployeeQualifications("student");
            }
            if (PasswordjTextField.getText().equals(PasswordjTextField.getText())) {
                x.SetPassWord(PasswordjTextField.getText());
            }
            if (!flag) {
                return;
            }

            if (x.UpdateEmployee()) {
                Truelabel.setText("Updated Successfully ... !");
                ResetPanelData();
                SearchEmployeejTextField.setText("");
            } else {
                Truelabel.setText("Failed to Update ... !");
            }
        } else {
            Truelabel.setText("Missing required Fields !");
        }


    }//GEN-LAST:event_UpdatejButtonActionPerformed

